# 函数的定义和特性
def weather():
    print("今天是2022-04-25")
    print("今天的天气：晴")
    print("今天的钞票:缺")


weather()


def weather02(day, wea, yuan):
    print(f"今天是{day}")
    print(f"今天的天气{wea}")
    print(f"今天的钞票{yuan}")


weather02("2022-05-01", "晴朗", "不缺")
