"""
    练习：模拟超市购物，用户输入购物金额，
    如果金额大于1000，给用户打8折，并输出打折后的金额
"""
money = int(input("请输入您的购物金额："))
if money > 1000:
    print(f"给您打了8折，折后价为{money * 0.8}")
else:
    print("您的金额没有打折")
