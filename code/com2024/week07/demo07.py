"""
    练习：输出100-999之间的水仙花数-153，
    个位的三次方+十位的三次方+百位三次方=这个数
    1.遍历100-1000之间的所有数
    2.拆分个位、十位、百位
    3.判断这个数是否是水仙花数
"""
for num in range(100, 1000):
    # 个位
    ge = num % 10
    # 十位
    shi = num // 10 % 10
    # 百位
    bai = num // 100
    if ge**3 + shi**3 + bai** 3 == num:
        print(num)

