"""
打印【100，1000】的 "水仙花数 "，所谓 "水仙花数 "是指一个三位数，其各位数字立方和等于该数本身。

"""
i = 100
while i < 1000:

# ********** Begin *********

    # 分离出各位，十位，百位
    bai = i//100
    shi = i//10 % 10
    ge = i % 10
    he = bai**3 + shi**3 + ge**3

# ********** End *********

    if he == i:
        print('{}是水仙花数'.format(i))
    i += 1